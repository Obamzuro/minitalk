int	ft_is_prime(int nb)
{
	int i;

	if (nb == 1 || nb <= 0)
		return (0);
	if (nb < 4)
		return (1);
	if (nb % 2 == 0)
		return (0);
	if (nb < 9)
		return (1);
	if (nb % 3 == 0)
		return (0);
	i = 5;
	while (i <= nb / 2)
	{
		if (nb % i == 0)
			return (0);
		if (nb % (i + 2) == 0)
			return (0);
		i += 6;
	}
	return (1);
}
