#include "libft.h"

char	*ft_strncat(char *s1, const char *s2, size_t n)
{
	char *c;

	c = s1 + ft_strlen(s1);
	while (n-- && *s2)
		*c++ = *s2++;
	*c = 0;
	return (s1);
}
